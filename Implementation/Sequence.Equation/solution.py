def permutationEquation(p):
    # Write your code here
    res = []
    
    n = len(p)
    for i in range(1, n + 1):
        y = p.index(i);
        x = p.index(y + 1);
        res.append(x + 1);
        
    
    
    return res

if __name__ == '__main__':
    fptr = open(os.environ['OUTPUT_PATH'], 'w')

    n = int(input().strip())

    p = list(map(int, input().rstrip().split()))

    result = permutationEquation(p)

